    private boolean numeroValido(char c, KeyEvent evt, String t)
    {
        if (!(Character.isDigit(c) || c == KeyEvent.VK_PERIOD)) 
        {
            return false;
        }
        if(t.contains(".") && c == KeyEvent.VK_PERIOD)
        {
            return false;
        }
        else
        {
            return true;
        }
    }
    
